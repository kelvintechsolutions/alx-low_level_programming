/**
  * _strncpy - Copy a string.
  * @dest:  destination value.
  * @src:  source value.
  * @n:  copy limit.
  *
  * Return: char value.
  */
char *_strncpy(char *dest, char *src, int n)
{
	int i;

	i = 0;

	while (src[i] != '\0' && i < n)
	{
		dest[i] = src[i];
		i++;
	}

	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}

	return (dest);
}
